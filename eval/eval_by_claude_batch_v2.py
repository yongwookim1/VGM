"""
Claude Batch API evaluation script for cleaned JSON format.

This version supports the new JSON format where:
- "query" field is directly available (no need to extract from conversations)
- "conversations" field may be absent or empty
- "prediction" field contains the model's response to evaluate

Key improvements over v1:
- Simplified query extraction (direct from "query" field)
- Removed unnecessary complexity in user_query fallback logic
- Cleaner code structure
"""

